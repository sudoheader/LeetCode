# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        lookup = {}
        for count, num in enumerate(nums):
            try:
                return lookup[num], count
            except KeyError:
                lookup.setdefault(target - num, count)
